from abc import ABC, abstractmethod

class Loader(ABC):
    def __init__(self):
        pass

    def _load():
        files = self.configure()

    @abstractmethod
    def decompose_file():
        pass

    @abstractmethod
    def configure():
        pass

    @abstractmethod
    def timestamp_to_file():
        pass

    @abstractmethod
    def file_to_timestamp():
        pass
